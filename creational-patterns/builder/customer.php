<?php
class Customer{
    
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $firstName;

    /**
     * @var string
     */
    private $lastName;

    /**
     * @var string
     */
    private $email;

    /**
     * @var int
     */
    private $age;
    
    public function __construct($id, $firstName, $lastName, $email, $age)
    {
        $this->id;
        $this->firstName;
        $this->lastName;
        $this->email;
        $this->age;
    }
}